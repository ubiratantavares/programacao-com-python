"""
Faça um programa e python que receba dois numeros e ao final mostre a soma,
subtração, multiplicação e a divisão dos numeros lidos.
"""

num1 = int(input("Digite o primeiro numero: "))
num2 = int(input("Digite o segundo numero: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print("Soma: {}".format(soma))
print("Subtracao: {}".format(subtracao))
print("Multiplicação: {}".format(multiplicacao))
print("Divisão: {}".format(divisao))

